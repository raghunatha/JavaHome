package com.java.basics.dataTypeAndVariables;

public class DefaultValues {
	
	static int i;
	static 	byte b;
	static long l;
	static short s;
	static float f;
	static char c;
	static double d;
	static boolean bool;
	static String str;
	
	public static void main(String args[]){
		
		System.out.println("The default value of the boolean = " + bool);
        System.out.println("The default value of the int     = " + i);
        System.out.println("The default value of the byte    = " + b);
        System.out.println("The default value of the long 	 = " + l);        
        System.out.println("The default value of the short   = " + s);       
        System.out.println("The default value of the float   = " + f);
        System.out.println("The default value of the char    = " + c);
        System.out.println("The default value of the double  = " + d);
        System.out.println("The default value of the String  = " + str);
	}

}
